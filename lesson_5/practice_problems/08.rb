hsh = {first: ['the', 'quick'], second: ['brown', 'fox'], third: ['jumped'], fourth: ['over', 'the', 'lazy', 'dog']}

hsh.values.each do |arr|
  arr.flatten.each do |word|
    word.chars.each do |letter|
      puts letter.scan(/[aeoui]/)
    end
  end
end

